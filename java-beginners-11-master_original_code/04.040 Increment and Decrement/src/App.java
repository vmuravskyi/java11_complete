
public class App {

	public static void main(String[] args) {

		int count = 0;
		
		System.out.println(count);

		count = count + 1;

		System.out.println(count);

		count += 1;

		System.out.println(count);

		count++;

		System.out.println(count);

		++count;

		System.out.println(count);

		count = count - 1;

		System.out.println(count);

		count -= 1;

		System.out.println(count);
		
		count--;
		
		System.out.println(count);
		
		--count;
		
		System.out.println(count);

	}

}
